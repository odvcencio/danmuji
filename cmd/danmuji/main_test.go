func TestBuildRecursive(t *testing.T) {
	tmpDir := t.TempDir()
	writeFile(t, filepath.Join(tmpDir, "example_test.dmj"), `package example_test

import "testing"

unit "hello" {
	then "works" {
		expect 1 == 1
	}
}
`)
	writeFile(t, filepath.Join(tmpDir, "nested", "service_test.dmj"), `package nested_test

import "testing"

unit "nested" {
	then "works" {
		expect true
	}
}
`)

	if err := build(tmpDir, false); err != nil {
		t.Fatalf("build: %v", err)
	}

	for _, outputFile := range []string{
		filepath.Join(tmpDir, "example_danmuji_test.go"),
		filepath.Join(tmpDir, "nested", "service_danmuji_test.go"),
	} {
		content, err := os.ReadFile(outputFile)
		if err != nil {
			t.Fatalf("read output %s: %v", outputFile, err)
		}
		if !strings.Contains(string(content), "Code generated by danmuji") {
			t.Fatalf("expected generated header in %s", outputFile)
		}
	}
}

