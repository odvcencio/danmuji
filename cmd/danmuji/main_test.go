func TestFmtDanmujiRecursive(t *testing.T) {
	tmpDir := t.TempDir()
	path := filepath.Join(tmpDir, "nested", "format_test.dmj")
	writeFile(t, path, `package main_test

import "testing"

unit "test" {
  given "a value" {
    x := 42
      then "it is 42" {
        expect x == 42
      }
  }
}
`)

	if err := fmtDanmuji(tmpDir); err != nil {
		t.Fatalf("fmtDanmuji: %v", err)
	}

	content, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("read formatted file: %v", err)
	}
	if !strings.Contains(string(content), "\t") {
		t.Fatal("expected tab indentation after formatting")
	}
}
