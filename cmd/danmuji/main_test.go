func TestRunTestCommandRecursive(t *testing.T) {
	tmpDir := newTestModule(t)
	writeFile(t, filepath.Join(tmpDir, "nested", "math.dmj"), `package nested_test

import "testing"

unit "addition" {
	then "one plus one" {
		expect 1+1 == 2
	}
}
`)

	if code := runTest([]string{tmpDir}); code != 0 {
		t.Fatalf("runTest returned %d", code)
	}

	generatedFile := filepath.Join(tmpDir, "nested", "math_danmuji_test.go")
	if _, err := os.Stat(generatedFile); !os.IsNotExist(err) {
		t.Fatalf("expected generated file %s to be cleaned up", generatedFile)
	}
}

